package fr.lasschko.pocketimperium.pocketimperium.model;

/**
 * Enumération représentant les différents niveaux de système dans le jeu.
 * Chaque niveau correspond à un type de système dans le jeu, allant de
 * LEVEL_0 à LEVEL_3.
 */
public enum SystemLevel {
    /**
     * Niveau 0, représentant un système de niveau inférieur ou non développé.
     */
    LEVEL_0,

    /**
     * Niveau 1, représentant un système modérément développé.
     */
    LEVEL_1,

    /**
     * Niveau 2, représentant un système plus développé.
     */
    LEVEL_2,

    /**
     * Niveau 3, représentant un système avancé ou hautement développé.
     */
    LEVEL_3;
}
